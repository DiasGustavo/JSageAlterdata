public class EmpresaAD {
    
    int cdempresa;
    String nmempresa;
    String nmfantasia;
    boolean stativa;
    String nmnomecompleto;
    String nrcgc;

    public String getNrcgc() {
        return nrcgc;
    }

    public void setNrcgc(String nrcgc) {
        this.nrcgc = nrcgc;
    }

    public int getCdempresa() {
        return cdempresa;
    }

    public void setCdempresa(int cdempresa) {
        this.cdempresa = cdempresa;
    }

    public String getNmempresa() {
        return nmempresa;
    }

    public void setNmempresa(String nmempresa) {
        this.nmempresa = nmempresa;
    }

    public String getNmfantasia() {
        return nmfantasia;
    }

    public void setNmfantasia(String nmfantasia) {
        this.nmfantasia = nmfantasia;
    }

    public boolean isStativa() {
        return stativa;
    }

    public void setStativa(boolean stativa) {
        this.stativa = stativa;
    }

    public String getNmnomecompleto() {
        return nmnomecompleto;
    }

    public void setNmnomecompleto(String nmnomecompleto) {
        this.nmnomecompleto = nmnomecompleto;
    }
  
  
  
    
}
